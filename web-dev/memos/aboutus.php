          <h1>memos</h1>
          <p>MEMOS started in 2021.</p>
          <p>
            It is a Danish company created by 3 young men, Simon Nicolaisen,
            Martin Maigaard and Mohammad Rashed.
          </p>
          <p>
            These 3 men had each a watch that meant something for them but did
            not symbolize something special. Therefor they started to imagine a
            watch which show what they wanted to tell.
          </p>
          <p>
            All love their city where they come from. It is why they decided to
            take it as a symbol on the watch. It shows to other people their
            love for the city and the special meaning it has for them. They
            could tell their own and unique story to other people. They wanted
            to let the possibility to people to be able to do the same.<br />Maybe
            the next watch is going to be your city so you can also tell your
            own story.
          </p>
        </section>
        <section>
          <h2>Values of the watches</h2>
          <p>
            Today, everything is going fast and something we forget to live in
            the present. This watch can bring you back to it. MEMOS wanted to
            create a special watch that connects people and that tells a unique
            story about where they come from.
          </p>
          <p>
            The owner of it can tell its own story. It is why it is made in a
            simple and elegant design in a limited edition to make them live for
            generations.
          </p>
          <p>
            The watches are design in Denmark by the founders of the company.
          </p>
